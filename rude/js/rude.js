window.onload = function() {
	var place = Math.floor(Math.random() * places.length);
	var coords = new L.LatLng(places[place].lat, places[place].lon);
	var marker = new L.Marker(coords, {
		title: places[place].label
	});
	var layer = new L.StamenTileLayer("toner");
	var map = new L.Map("map", {
		layers: layer
	});
	
	var coords = [];
	var markers = [];
	var icon = new L.Icon({
		iconUrl: '/images/signpost-icon.png',
		iconSize: [32, 37]
	});
	for (var i=0; i < places.length; i++) {
		var ll = new L.LatLng(places[i].lat, places[i].lon);
		var mkr = new L.Marker(ll, {icon: icon});
		var label = places[i].label;
		mkr.bindPopup(label);
		mkr.on('mouseover', function(e) {
			e.target.openPopup();
		});
		coords.push(ll);
		markers.push(mkr);
		mkr.addTo(map);
	}
	
	var bounds = new L.LatLngBounds(coords);
	map.fitBounds(bounds);
}